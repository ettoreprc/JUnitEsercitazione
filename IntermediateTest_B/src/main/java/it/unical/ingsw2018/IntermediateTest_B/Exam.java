package it.unical.ingsw2018.IntermediateTest_B;


public class Exam {
    
	private String name;
	private int grade;
	private int cfu;
	
	public Exam(String name, int grade, int cfu) {
		super();
		this.name = name;
		this.grade = grade;
		this.cfu = cfu;
	}

	public String getName() {
		return name;
	}

	public int getGrade() {
		return grade;
	}

	public int getCfu() {
		return cfu;
	}

	
	
	
	
	
}
